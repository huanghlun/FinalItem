package com.finalItem.domain;

public class Rat {
	Integer rat_id;
	String rat_name;
	String rat_description;
	String rat_head;
	Integer good_num;
	public Integer getRat_id() {
		return rat_id;
	}
	public void setRat_id(Integer rat_id) {
		this.rat_id = rat_id;
	}
	public String getRat_name() {
		return rat_name;
	}
	public void setRat_name(String rat_name) {
		this.rat_name = rat_name;
	}
	public String getRat_description() {
		return rat_description;
	}
	public void setRat_description(String rat_description) {
		this.rat_description = rat_description;
	}
	public String getRat_head() {
		return rat_head;
	}
	public void setRat_head(String rat_head) {
		this.rat_head = rat_head;
	}
	public Integer getGood_num() {
		return good_num;
	}
	public void setGood_num(Integer good_num) {
		this.good_num = good_num;
	}
	
	
}
